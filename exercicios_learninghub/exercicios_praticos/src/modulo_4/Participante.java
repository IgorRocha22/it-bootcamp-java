package modulo_4;

public class Participante {
    int id;
    int idade;

    public Participante(int id, int idade) {
        this.id = id;
        this.idade = idade;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
